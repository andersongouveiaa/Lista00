public class Tretangulares {

    private double lado;
    private double altura;

    public Tretangulares(double lado, double altura) {
        this.altura = altura;
        this.lado = lado;
    }

    public double getLado() {
        return lado;
    }

    public void setLado(double lado) {
        this.lado = lado;
    }

    public double getAltura() {
        return altura;
    }

    public void setAltura(double altura) {
        this.altura = altura;
    }
}
